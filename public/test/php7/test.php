//位运算
/*
 * 2
 * 10
 * 00000000 0000000 00000000 00000010
 *
 * 3
 * 00000000 0000000 00000000 00000011
 * 按位与 &
 * 2 & 3
 * 00000000 0000000 00000000 00000010
 * */

//按位与：只有当都为1时才为1 &
/*
00000000 0000000 00000000 00000010  2
00000000 0000000 00000000 00000011  3
00000000 0000000 00000000 00000010  2  result
*/
var_dump(2 & 3);

//按位或：只有当都为0时才为0 |
/*
00000000 0000000 00000000 00000010  2
00000000 0000000 00000000 00000011  3
00000000 0000000 00000000 00000011  3  result
*/
var_dump(2 | 3);

//按位异或：只有参与运算当两位不同，运算结果才为1，否则为0 ^
/*
00000000 0000000 00000000 00000010  2
00000000 0000000 00000000 00000011  3
00000000 0000000 00000000 00000001  1  result
*/
var_dump(2 ^ 3);

//按位非：？补码反码？ ～
/* ～2
00000000 0000000 00000000 00000010  2
00000000 0000000 00000000 00000001  1  result
*/
var_dump(~2);

//移位运算：只有参与运算当两位不同，运算结果才为1，否则为0 ^
/*
00000000 0000000 00000000 00000001  1
00000000 0000000 00000000 00000100  4  result
*/
echo '<< 左移';
var_dump(1 << 2);
var_dump(3 << 2);
echo '>> 右移';
var_dump(1 >> 2);
var_dump(3 >> 2);
var_dump(7 >> 2);
var_dump(15 >> 2);


